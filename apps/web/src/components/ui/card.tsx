import * as React from "react";
import { cn } from "~/lib/utils";
import { motion } from "framer-motion";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { withMotion?: boolean }
>(({ className, withMotion = false, ...props }, ref) => {
  const cardContent = (
    <div
      ref={ref}
      className={cn(
        "rounded-lg border border-linear-border bg-linear-elevated text-linear-text shadow-elevation-1 transition-all duration-150",
        className
      )}
      {...props}
    />
  );

  if (withMotion) {
    return (
      <motion.div
        initial={{ opacity: 0, y: 8 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.2, ease: [0.25, 0.1, 0.25, 1] }}
      >
        {cardContent}
      </motion.div>
    );
  }

  return cardContent;
});
Card.displayName = "Card";

