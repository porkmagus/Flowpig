const ALL_EMOJIS = Object.values(EMOJI_CATEGORIES).flat();

interface EmojiPickerProps {
  value: string;
  onChange: (emoji: string) => void;
  onClose: () => void;
}

export function EmojiPicker({ value, onChange, onClose }: EmojiPickerProps) {
  const [search, setSearch] = useState('');
  const [activeCategory, setActiveCategory] = useState('Faces');
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleClick = (e: MouseEvent) => {
      if (containerRef.current && !containerRef.current.contains(e.target as Node)) {
        onClose();
      }
    };
    document.addEventListener('mousedown', handleClick);
    return () => document.removeEventListener('mousedown', handleClick);
  }, [onClose]);

  const filtered = search.trim()
    ? ALL_EMOJIS.filter((e) => e.includes(search.trim()))
    : null;

  return (
    <div
      ref={containerRef}
      className="absolute z-50 w-80 rounded-xl border border-linear-border bg-linear-elevated shadow-xl shadow-black/40 p-3"
    >
      <div className="flex items-center gap-2 mb-3">
        <Search className="w-4 h-4 text-linear-text-tertiary" />
        <input
          type="text"
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Search emoji..."
          className="flex-1 bg-transparent text-sm text-linear-text placeholder:text-linear-text-tertiary outline-none"
          autoFocus
        />
        {search && (
          <button onClick={() => setSearch('')} className="text-linear-text-tertiary hover:text-linear-text">
            <X className="w-3.5 h-3.5" />
          </button>
        )}
      </div>

      {filtered ? (
        <div className="grid grid-cols-8 gap-1 max-h-48 overflow-y-auto">
          {filtered.map((emoji) => (
            <button
              key={emoji}
              onClick={() => onChange(emoji)}
              className={`text-xl p-1 rounded hover:bg-linear-surface transition-colors ${value === emoji ? 'bg-linear-accent/20' : ''}`}
            >
              {emoji}
            </button>
          ))}
        </div>
      ) : (
        <>
          <div className="flex gap-1 mb-2 overflow-x-auto pb-1">
            {Object.keys(EMOJI_CATEGORIES)
              .filter((c) => c !== 'Recent')
              .map((cat) => (
                <button
                  key={cat}
                  onClick={() => setActiveCategory(cat)}
                  className={`text-xs px-2 py-1 rounded-md whitespace-nowrap transition-colors ${
                    activeCategory === cat
                      ? 'bg-linear-surface text-linear-text'
                      : 'text-linear-text-tertiary hover:text-linear-text'
                  }`}
                >
                  {cat}
                </button>
              ))}
          </div>
          <div className="grid grid-cols-8 gap-1 max-h-48 overflow-y-auto">
            {EMOJI_CATEGORIES[activeCategory]?.map((emoji) => (
              <button
                key={emoji}
                onClick={() => onChange(emoji)}
                className={`text-xl p-1 rounded hover:bg-linear-surface transition-colors ${value === emoji ? 'bg-linear-accent/20' : ''}`}
              >
                {emoji}
              </button>
            ))}
          </div>
        </>
      )}
    </div>
  );
}
